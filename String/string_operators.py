# string Operators
#  + concatenation
#  * repeat
#  in
#  not in
first_name ="Arshika"
second_name ="Kumari"
print(first_name +" " + second_name)
print(first_name * 2)
print(first_name * 3)
print(first_name  in second_name)
print(first_name  not in second_name)
print(first_name  in "Arshika")
print(second_name in "Arshika")